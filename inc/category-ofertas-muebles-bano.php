/**
 * Badge con porcentaje de descuento real (-XX%) para productos en oferta
 * de la categor&iacute;a 2528. Reemplaza el "Sale!" gen&eacute;rico de WooCommerce.
 */
function adrihosan_ofertas_muebles_bano_sale_flash( $html, $post, $product ) {
    if ( ! is_product_category( 2528 ) ) {
        return $html;
    }
    if ( ! $product->is_on_sale() ) {
        return $html;
    }

    $regular = (float) $product->get_regular_price();
    $sale    = (float) $product->get_sale_price();

    if ( $regular <= 0 || $sale <= 0 || $sale >= $regular ) {
        return $html;
    }

    $percent = round( ( ( $regular - $sale ) / $regular ) * 100 );

    return '<span class="onsale adrihosan-sale-badge">-' . esc_html( $percent ) . '%</span>';
}

